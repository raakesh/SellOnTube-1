// Helper: get publish date for a topic slug (falls back to topic.publishDate)
export function getTopicPublishDate(slug: string, fallback: string): string {
  return topicPublishSchedule[slug] ?? fallback;
}

// Helper: check if a topic is live
export function isTopicLive(slug: string, fallback: string): boolean {
  const date = getTopicPublishDate(slug, fallback);
  const cutoff = new Date();
  cutoff.setHours(23, 59, 59, 999);
  return new Date(date + 'T00:00:00+05:30') <= cutoff;
}

// Helper: get topics by industry
export function getTopicsByIndustry(industry: Topic['industry']): Topic[] {
  return topics.filter((t) => t.industry === industry);
}

// Helper: get related topics by slug array
export function getRelatedTopics(slugs: string[]): Topic[] {
  return slugs
    .map((slug) => topics.find((t) => t.slug === slug))
    .filter((t): t is Topic => t !== undefined);
}

